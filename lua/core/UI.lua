-- Enable 24-bit RGB colors
vim.o.termguicolors = true

-- Set screen lines to keep around cursor
vim.o.scrolloff = 20

-- Show current position
vim.o.ruler = true

-- Split window to the right
vim.o.splitright = true

-- Show matching brackets when cursor is on them
vim.o.showmatch = true

-- Blink time of matching bracket in tenths of a second
vim.o.matchtime = 2

-- Show line number
vim.o.number = true
